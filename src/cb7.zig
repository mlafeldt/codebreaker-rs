test "cb7 encrypt_code" {
    for (cb_tests) |case| {
        var cb = Cb7.init();
        cb.beefcode(case.beefcode[0], case.beefcode[1]);

        for (case.decrypted, 0..) |code, idx| {
            const result = cb.encryptCode(code[0], code[1]);
            try std.testing.expectEqual(case.encrypted[idx], result);
            if (isBeefcode(code[0])) {
                cb.beefcode(code[0], code[1]);
            }
        }
    }
}

