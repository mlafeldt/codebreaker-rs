/// Returns true if the code address indicates a "beefcode".
pub inline fn isBeefcode(addr: u32) bool {
    return addr & 0xffff_fffe == BEEFCODE;
}

/// Computes the multiplicative inverse of x modulo (2^32). x must be odd!
/// Based on Newton's method.
fn modInverse(x: u32) u32 {
    var y = x;
    // Call this recurrence formula 4 times for 32-bit values:
    // f(y) = y * (2 - y * x) modulo 2^32
    y = y *% (2 -% (y *% x));
    y = y *% (2 -% (y *% x));
    y = y *% (2 -% (y *% x));
    y = y *% (2 -% (y *% x));
    return y;
}

/// Multiplication, modulo (2^32)
inline fn mulEncrypt(a: u32, b: u32) u32 {
    return a *% (b | 1);
}

/// Multiplication with multiplicative inverse, modulo (2^32)
inline fn mulDecrypt(a: u32, b: u32) u32 {
    return a *% modInverse(b | 1);
}

/// Multiply two u64 values and take modulo, using u128 for intermediate result
inline fn mulMod(a: u64, b: u64, m: u64) u64 {
    const result: u128 = @as(u128, a) * @as(u128, b);
    return @truncate(result % m);
}

/// RSA encryption/decryption using modular exponentiation
/// Implements: result = base^exp mod modulus using square-and-multiply
fn rsaCrypt(addr: *u32, val: *u32, rsakey: u64, modulus: u64) void {

    // Setup base = (addr << 32) | val
    const base_val: u64 = (@as(u64, addr.*) << 32) | val.*;

    // Exponentiation is only invertible if base < modulus
    if (base_val >= modulus) return;

    // Implement modular exponentiation using square-and-multiply
    var result: u64 = 1;
    var base = base_val;
    var exp = rsakey;

    while (exp > 0) {
        if (exp & 1 != 0) {
            result = mulMod(result, base, modulus);
        }
        base = mulMod(base, base, modulus);
        exp >>= 1;
    }

    addr.* = @truncate(result >> 32);
    val.* = @truncate(result);
}

pub const Cb7 = struct {
    seeds: [5][256]u8,
    key: [5]u32,
    beefcodf: bool,
    initialized: bool,
    allocator: std.mem.Allocator,

    /// Returns a new processor for encrypting and decrypting CB v7+ codes.
    pub fn init(allocator: std.mem.Allocator) Cb7 {
        return Cb7{
            .seeds = ZERO_SEEDS,
            .key = [_]u32{0} ** 5,
            .beefcodf = false,
            .initialized = false,
            .allocator = allocator,
        };
    }

    /// Returns a new processor with default CB v7 encryption (CMGSCCC.com).
    pub fn initDefault(allocator: std.mem.Allocator) Cb7 {
        var cb7 = init(allocator);
        cb7.beefcode(BEEFCODE, 0);
        return cb7;
    }

    /// Generates or changes the encryption key and seeds.
    pub fn beefcode(self: *Cb7, addr: u32, val: u32) void {
        std.debug.assert(isBeefcode(addr));

        // Easily access all bytes of val as indices into seeds
        const val_bytes = std.mem.toBytes(val);
        const idx = [4]usize{
            val_bytes[0],
            val_bytes[1],
            val_bytes[2],
            val_bytes[3],
        };

        // Set up key and seeds
        if (!self.initialized) {
            @memcpy(&self.key, &RC4_KEY);

            if (val != 0) {
                @memcpy(&self.seeds, &SEEDS);
                for (0..4) |i| {
                    self.key[i] = (@as(u32, self.seeds[(i + 3) % 4][idx[3]]) << 24) |
                        (@as(u32, self.seeds[(i + 2) % 4][idx[2]]) << 16) |
                        (@as(u32, self.seeds[(i + 1) % 4][idx[1]]) << 8) |
                        @as(u32, self.seeds[i % 4][idx[0]]);
                }
            } else {
                @memcpy(&self.seeds, &ZERO_SEEDS);
            }

            self.initialized = true;
        } else if (val != 0) {
            for (0..4) |i| {
                self.key[i] = (@as(u32, self.seeds[(i + 3) % 4][idx[3]]) << 24) |
                    (@as(u32, self.seeds[(i + 2) % 4][idx[2]]) << 16) |
                    (@as(u32, self.seeds[(i + 1) % 4][idx[1]]) << 8) |
                    @as(u32, self.seeds[i % 4][idx[0]]);
            }
        } else {
            // Special case for 2x BEEFC0DE 00000000 in a row
            @memcpy(&self.seeds, &ZERO_SEEDS);
            self.key[0] = 0;
            self.key[1] = 0;
            self.key[2] = 0;
            self.key[3] = 0;
        }

        // Use key to encrypt seeds with RC4
        for (0..5) |i| {
            const key_bytes = std.mem.sliceAsBytes(&self.key);
            var rc4 = Rc4.init(key_bytes);
            // Encrypt seeds
            rc4.crypt(&self.seeds[i]);
            // Encrypt original key for next round
            const key_buf = std.mem.sliceAsBytes(&self.key);
            rc4.crypt(key_buf);
        }

        // Since we don't know the extra seed value of BEEFC0DF yet,
        // all we can do is set a flag.
        self.beefcodf = addr & 1 != 0;
    }

    /// Encrypts a code and returns the result.
    pub fn encryptCode(self: *Cb7, addr: u32, val: u32) Code {
        var new_addr = addr;
        var new_val = val;
        self.encryptCodeMut(&new_addr, &new_val);
        return .{ .addr = new_addr, .val = new_val };
    }

    /// Encrypts a code directly.
    pub fn encryptCodeMut(self: *Cb7, addr: *u32, val: *u32) void {
        const oldaddr = addr.*;
        const oldval = val.*;

        // Step 1: Multiplication, modulo (2^32)
        addr.* = mulEncrypt(addr.*, self.key[0] -% self.key[1]);
        val.* = mulEncrypt(val.*, self.key[2] +% self.key[3]);

        // Step 2: RC4
        var code = [2]u32{ addr.*, val.* };
        const key_bytes = std.mem.sliceAsBytes(&self.key);
        var rc4 = Rc4.init(key_bytes);
        rc4.crypt(std.mem.sliceAsBytes(&code));
        addr.* = code[0];
        val.* = code[1];

        // Step 3: RSA
        rsaCrypt(addr, val, RSA_ENC_KEY, RSA_MODULUS);

        // Step 4: Encryption loop of 64 cycles, using the generated seeds
        const s = std.mem.bytesAsSlice(u32, std.mem.sliceAsBytes(&self.seeds));
        for (0..64) |i| {
            addr.* = (addr.* +% s[2 * 64 + i] ^ s[i]) -% (val.* ^ s[4 * 64 + i]);
            val.* = (val.* -% s[3 * 64 + i] ^ s[64 + i]) +% (addr.* ^ s[4 * 64 + i]);
        }

        // BEEFC0DE
        if (isBeefcode(oldaddr)) {
            self.beefcode(oldaddr, oldval);
            return;
        }

        // BEEFC0DF uses two codes. If the previous code was the first of the
        // two, use the current one to encrypt the seeds.
        if (self.beefcodf) {
            const code_bytes = std.mem.sliceAsBytes(&[2]u32{ oldaddr, oldval });
            var rc4_beefcodf = Rc4.init(code_bytes);
            rc4_beefcodf.crypt(std.mem.sliceAsBytes(&self.seeds));
            self.beefcodf = false;
        }
    }

    /// Decrypts a code and returns the result.
    pub fn decryptCode(self: *Cb7, addr: u32, val: u32) Code {
        var new_addr = addr;
        var new_val = val;
        self.decryptCodeMut(&new_addr, &new_val);
        return .{ .addr = new_addr, .val = new_val };
    }

    /// Decrypts a code directly.
    pub fn decryptCodeMut(self: *Cb7, addr: *u32, val: *u32) void {
        // Step 1: Decryption loop of 64 cycles, using the generated seeds
        const s = std.mem.bytesAsSlice(u32, std.mem.sliceAsBytes(&self.seeds));
        var i: usize = 64;
        while (i > 0) {
            i -= 1;
            val.* = (val.* -% (addr.* ^ s[4 * 64 + i]) ^ s[64 + i]) +% s[3 * 64 + i];
            addr.* = (addr.* +% (val.* ^ s[4 * 64 + i]) ^ s[i]) -% s[2 * 64 + i];
        }

        // Step 2: RSA
        rsaCrypt(addr, val, RSA_DEC_KEY, RSA_MODULUS);

        // Step 3: RC4
        var code = [2]u32{ addr.*, val.* };
        const key_bytes = std.mem.sliceAsBytes(&self.key);
        var rc4 = Rc4.init(key_bytes);
        rc4.crypt(std.mem.sliceAsBytes(&code));
        addr.* = code[0];
        val.* = code[1];

        // Step 4: Multiplication with multiplicative inverse, modulo (2^32)
        addr.* = mulDecrypt(addr.*, self.key[0] -% self.key[1]);
        val.* = mulDecrypt(val.*, self.key[2] +% self.key[3]);

        // BEEFC0DF uses two codes. If the previous code was the first of the
        // two, use the current one to decrypt the seeds.
        if (self.beefcodf) {
            const code_bytes = std.mem.sliceAsBytes(&[2]u32{ addr.*, val.* });
            var rc4_beefcodf = Rc4.init(code_bytes);
            rc4_beefcodf.crypt(std.mem.sliceAsBytes(&self.seeds));
            self.beefcodf = false;
            return;
        }

        // BEEFC0DE
        if (isBeefcode(addr.*)) {
            self.beefcode(addr.*, val.*);
        }
    }
};

