const std = @import("std");
const Rc4 = @import("rc4.zig").Rc4;

pub const Cb7 = struct {
    seeds: [5][256]u8,
    key: [5]u32,
    beefcodf: bool,
    initialized: bool,

    pub fn init() Cb7 {
        return .{
            .seeds = ZERO_SEEDS,
            .key = [_]u32{0} ** 5,
            .beefcodf = false,
            .initialized = false,
        };
    }

    pub fn initDefault() Cb7 {
        var cb = Cb7.init();
        cb.beefcode(BEEFCODE, 0);
        return cb;
    }

    pub fn beefcode(self: *Cb7, addr: u32, val: u32) void {
        std.debug.assert(isBeefcode(addr));

        var idx: [4]usize = undefined;
        const val_le = std.mem.nativeToLittle(u32, val);
        const bytes = @as([4]u8, @bitCast(val_le));
        for (bytes, 0..) |b, i| {
            idx[i] = b;
        }

        if (!self.initialized) {
            self.key = RC4_KEY;

            if (val != 0) {
                self.seeds = SEEDS;
                self.reseedKey(idx);
            } else {
                self.seeds = ZERO_SEEDS;
            }

            self.initialized = true;
        } else if (val != 0) {
            self.reseedKey(idx);
        } else {
            self.seeds = ZERO_SEEDS;
            for (self.key[0..4]) |*slot| {
                slot.* = 0;
            }
        }

        const key_bytes = std.mem.sliceAsBytes(self.key[0..]);
        for (self.seeds[0..]) |*seed| {
            var rc4 = Rc4.init(key_bytes);
            rc4.crypt(seed.*[0..]);
            rc4.crypt(key_bytes);
        }

        self.beefcodf = (addr & 1) != 0;
    }

    fn reseedKey(self: *Cb7, idx: [4]usize) void {
        for (0..4) |i| {
            self.key[i] =
                (@as(u32, self.seeds[(i + 3) % 4][idx[3]]) << 24) |
                (@as(u32, self.seeds[(i + 2) % 4][idx[2]]) << 16) |
                (@as(u32, self.seeds[(i + 1) % 4][idx[1]]) << 8) |
                @as(u32, self.seeds[i % 4][idx[0]]);
        }
    }

    pub fn encryptCode(self: *Cb7, addr: u32, val: u32) [2]u32 {
        var a = addr;
        var v = val;
        self.encryptCodeMut(&a, &v);
        return .{ a, v };
    }

    pub fn encryptCodeMut(self: *Cb7, addr: *u32, val: *u32) void {
        const oldaddr = addr.*;
        const oldval = val.*;

        addr.* = mulEncrypt(addr.*, self.key[0] -% self.key[1]);
        val.* = mulEncrypt(val.*, self.key[2] +% self.key[3]);

        var block = [2]u32{ addr.*, val.* };
        const key_bytes = std.mem.sliceAsBytes(self.key[0..]);
        var rc4 = Rc4.init(key_bytes);
        rc4.crypt(std.mem.sliceAsBytes(block[0..]));
        addr.* = block[0];
        val.* = block[1];

        rsaCrypt(addr, val, RSA_ENC_KEY, RSA_MODULUS);

        const seeds_bytes = std.mem.sliceAsBytes(self.seeds[0..]);
        const s = std.mem.bytesAsSlice(u32, seeds_bytes);
        for (0..64) |i| {
            addr.* = ((addr.* +% s[2 * 64 + i]) ^ s[i]) -% (val.* ^ s[4 * 64 + i]);
            val.* = ((val.* -% s[3 * 64 + i]) ^ s[64 + i]) +% (addr.* ^ s[4 * 64 + i]);
        }

        if (isBeefcode(oldaddr)) {
            self.beefcode(oldaddr, oldval);
            return;
        }

        if (self.beefcodf) {
            var beef_seed = [2]u32{ oldaddr, oldval };
            var beef_rc4 = Rc4.init(std.mem.sliceAsBytes(beef_seed[0..]));
            beef_rc4.crypt(std.mem.sliceAsBytes(self.seeds[0..]));
            self.beefcodf = false;
        }
    }

    pub fn decryptCode(self: *Cb7, addr: u32, val: u32) [2]u32 {
        var a = addr;
        var v = val;
        self.decryptCodeMut(&a, &v);
        return .{ a, v };
    }

    pub fn decryptCodeMut(self: *Cb7, addr: *u32, val: *u32) void {
        const seeds_bytes = std.mem.sliceAsBytes(self.seeds[0..]);
        const s = std.mem.bytesAsSlice(u32, seeds_bytes);
        var idx: usize = 64;
        while (idx > 0) {
            idx -= 1;
            val.* = ((val.* -% (addr.* ^ s[4 * 64 + idx])) ^ s[64 + idx]) +% s[3 * 64 + idx];
            addr.* = ((addr.* +% (val.* ^ s[4 * 64 + idx])) ^ s[idx]) -% s[2 * 64 + idx];
        }

        rsaCrypt(addr, val, RSA_DEC_KEY, RSA_MODULUS);

        var block = [2]u32{ addr.*, val.* };
        const key_bytes = std.mem.sliceAsBytes(self.key[0..]);
        var rc4 = Rc4.init(key_bytes);
        rc4.crypt(std.mem.sliceAsBytes(block[0..]));
        addr.* = block[0];
        val.* = block[1];

        addr.* = mulDecrypt(addr.*, self.key[0] -% self.key[1]);
        val.* = mulDecrypt(val.*, self.key[2] +% self.key[3]);

        if (self.beefcodf) {
            var beef_seed = [2]u32{ addr.*, val.* };
            var beef_rc4 = Rc4.init(std.mem.sliceAsBytes(beef_seed[0..]));
            beef_rc4.crypt(std.mem.sliceAsBytes(self.seeds[0..]));
            self.beefcodf = false;
            return;
        }

        if (isBeefcode(addr.*)) {
            self.beefcode(addr.*, val.*);
        }
    }
};

pub fn isBeefcode(addr: u32) bool {
    return addr & 0xffff_fffe == BEEFCODE;
}

fn mulEncrypt(a: u32, b: u32) u32 {
    return a *% (b | 1);
}

fn mulDecrypt(a: u32, b: u32) u32 {
    return a *% modInverse(b | 1);
}

fn modInverse(x: u32) u32 {
    var y = x;
    inline for (0..4) |_| {
        y = y *% (2 -% y *% x);
    }
    return y;
}

fn rsaCrypt(addr: *u32, val: *u32, rsakey: u64, modulus: u64) void {
    const code = (@as(u64, addr.*) << 32) | @as(u64, val.*);
    if (code >= modulus) return;
    const result = modExp(code, rsakey, modulus);
    addr.* = @as(u32, @intCast(result >> 32));
    val.* = @as(u32, @intCast(result & 0xffff_ffff));
}

fn modExp(base: u64, exp: u64, modulus: u64) u64 {
    var result: u64 = 1 % modulus;
    var b = base % modulus;
    var e = exp;

    while (e != 0) {
        if ((e & 1) == 1) {
            result = mulMod(result, b, modulus);
        }
        e >>= 1;
        if (e != 0) {
            b = mulMod(b, b, modulus);
        }
    }

    return result;
}

fn mulMod(a: u64, b: u64, modulus: u64) u64 {
    const prod = @as(u128, a) * @as(u128, b);
    return @as(u64, @intCast(prod % @as(u128, modulus)));
}

